// Global ayah numbering (for audio CDN)
export const SURAH_STARTS = (() => {
  const starts = [1];
  for (let i = 0; i < SURAHS.length - 1; i++) {
    starts.push(starts[i] + SURAHS[i].verses);
  }
  return starts;
})();

export function getGlobalAyahNum(surahNum, ayahNum) {
  return SURAH_STARTS[surahNum - 1] + ayahNum - 1;
}

