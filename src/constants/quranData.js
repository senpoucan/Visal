export function parseTajweed(text) {
  if (!text) return [];
  // Matches [class[text] or [class:info[text]
  const regex = /\[([^\[\]]+)\[([^\[\]]+)\]/g;
  const parts = [];
  let lastIndex = 0;
  
  let match;
  while ((match = regex.exec(text)) !== null) {
    if (match.index > lastIndex) {
      parts.push({ text: text.substring(lastIndex, match.index), class: null });
    }
    const classInfo = match[1];
    const className = classInfo.split(':')[0]; // get pure class letter
    parts.push({ text: match[2], class: className });
    lastIndex = match.index + match[0].length;
  }
  if (lastIndex < text.length) {
    parts.push({ text: text.substring(lastIndex), class: null });
  }
  return parts;
}

export function countArabicLetters(text) {
  if (!text) return 0;
  const matches = text.match(/[\u0621-\u063A\u0641-\u064A]/g);
  return matches ? matches.length : 0;
}
